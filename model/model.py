# This is just a u-net

'''

The network is built up as follows:

-> first, a convolutional layer is applied on the batch of noisy images, and position embeddings are computed for the noise levels
-> next, a sequence of downsampling stages are applied. Each downsampling stage consists of
    2 ResNet blocks + groupnorm + attention + residual connection + a downsample operation
-> at the middle of the network, again ResNet blocks are applied, interleaved with attention
-> next, a sequence of upsampling stages are applied. Each upsampling stage consists of
    2 ResNet blocks + groupnorm + attention + residual connection + an upsample operation
-> finally, a ResNet block followed by a convolutional layer is applied.
'''
from functools import partial

import torch
from einops import rearrange
from torch import nn
from Utils.ModelUtils import WeightStandardizedConv2d, exists, default, getSequnceOfDimensions, \
    SinusoidalPositionEmbeddings, PreNorm, LinearAttention, Residual, Downsample, Attention, Upsample


# Most basic block with conv + groupNorm + Silu activation
class Block(nn.Module):
    def __init__(self, dim_in, dim_out, groups_count=8):
        super().__init__()
        self.proj = WeightStandardizedConv2d(dim_in, dim_out, 3, 1)
        self.norm = nn.GroupNorm(groups_count, dim_out)
        self.act = nn.SiLU()

    def forward(self, x, scale_shift=None):
        x = self.proj(x)
        x = self.norm(x)

        if exists(scale_shift):
            scale, shift = scale_shift
            x = x * (scale + 1) + shift

        x = self.act(x)
        return x


# The residual block
class ResnetBlock(nn.Module):
    def __init__(self, dim_in, dim_out, time_emb_dim, groups=8):
        super().__init__()

        self.block1 = Block(dim_in, dim_out, groups)
        self.block2 = Block(dim_out, dim_out, groups)

        self.res_conv = nn.Conv2d(dim_in, dim_out, 1) if dim_out != dim_in else nn.Identity()

        self.mlp = nn.Sequential(
            nn.SiLU(), nn.Linear(time_emb_dim, dim_out * 2) if exists(time_emb_dim)
            else None
        )

    def forward(self, x, time_emb=None):
        scale_shift = None

        if exists(self.mlp) and exists(time_emb):
            time_emb = self.mlp(time_emb)
            time_emb = rearrange(time_emb, "b c -> b c 1 1")
            scale_shift = time_emb.chunk(2, dim=1)  # break along channel dim in half, from dim_out * 2 to dim_out

        h = self.block1(x, scale_shift)
        h = self.block2(h)
        h = h + self.res_conv(x)

        return h


class Unet(nn.Module):
    def __init__(self,
                 dim,               # dimension of time embedding
                 init_dim=None,
                 out_dim=None,      # channels of final output image
                 dim_mults=(1, 2, 4, 8),
                 channels=3,
                 resnet_block_groups=4, ):
        super().__init__()

        self.channels = channels  # channels in input image
        input_channels = channels

        init_dim = default(init_dim, dim)

        self.init_conv = nn.Conv2d(input_channels, init_dim, 1, padding=0)

        in_out, dims = getSequnceOfDimensions(init_dim, dim, dim_mults)
        block_klass = partial(ResnetBlock, groups=resnet_block_groups)

        # time embeddings
        time_dim = dim * 4

        self.time_mlp = nn.Sequential(
            SinusoidalPositionEmbeddings(dim),
            nn.Linear(dim, time_dim),
            nn.GELU(),
            nn.Linear(time_dim, time_dim),
        )

        # layers
        self.downs = nn.ModuleList([])
        self.ups = nn.ModuleList([])
        num_resolutions = len(in_out)  # depth of U-net

        for ind, (dim_in, dim_out) in enumerate(in_out):
            is_last = ind >= (num_resolutions - 1)

            self.downs.append(
                nn.ModuleList(
                    [
                        block_klass(dim_in, dim_in, time_emb_dim=time_dim),
                        block_klass(dim_in, dim_in, time_emb_dim=time_dim),
                        Residual(PreNorm(dim_in, LinearAttention(dim_in))),  # input x; apply group norm, then attention to x, then add x to it.
                        Downsample(dim_in, dim_out) if not is_last
                        else nn.Conv2d(dim_in, dim_out, 3, padding=1),
                    ]
                )
            )

        mid_dim = dims[-1]
        self.mid_block1 = block_klass(mid_dim, mid_dim, time_emb_dim=time_dim)
        self.mid_attn = Residual(PreNorm(mid_dim, Attention(mid_dim)))
        self.mid_block2 = block_klass(mid_dim, mid_dim, time_emb_dim=time_dim)

        for ind, (dim_in, dim_out) in enumerate(reversed(in_out)):
            is_last = ind == (len(in_out) - 1)

            self.ups.append(
                nn.ModuleList(
                    [
                        block_klass(dim_out + dim_in, dim_out, time_emb_dim=time_dim),
                        block_klass(dim_out + dim_in, dim_out, time_emb_dim=time_dim),
                        Residual(PreNorm(dim_out, LinearAttention(dim_out))),
                        Upsample(dim_out, dim_in)
                        if not is_last
                        else nn.Conv2d(dim_out, dim_in, 3, padding=1),
                    ]
                )
            )
        self.out_dim = default(out_dim, channels)

        self.final_res_block = block_klass(dim * 2, dim, time_emb_dim=time_dim)
        self.final_conv = nn.Conv2d(dim, self.out_dim, 1)

    def forward(self, x, time):
        x = self.init_conv(x)
        r = x.clone()

        t = self.time_mlp(time)

        h = []  # To store bridge connections

        for block1, block2, attn, downsample in self.downs:
            x = block1(x, t)
            h.append(x)

            x = block2(x, t)
            x = attn(x)
            h.append(x)

            x = downsample(x)

        x = self.mid_block1(x, t)
        x = self.mid_attn(x)
        x = self.mid_block2(x, t)

        for block1, block2, attn, upsample in self.ups:
            x = torch.cat((x, h.pop()), dim=1)  # concat along dim = 1 i.e. channels dimension
            x = block1(x, t)

            x = torch.cat((x, h.pop()), dim=1)
            x = block2(x, t)
            x = attn(x)

            x = upsample(x)

        x = torch.cat((x, r), dim=1)

        x = self.final_res_block(x, t)
        return self.final_conv(x)
